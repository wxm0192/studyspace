<p><b>动手实验-31</b> </p>
<p><b>JAVA 调用Redis服务 </b> </p>
<p>说明：  </p>
<p style="font-size:10px" >Redis ( REmote DIctionary Server) ，是一个key-value 存储系统，是跨平台的非关系型数据库，特点是存取速度特别快， 常被用做高速缓存。 </p>
<p style="font-size:10px" >Redis 是云环境中非常重要、非常基础的一个 PAAS 层服务， 是企业级应用开发过程中的关键组件 。  </p>

<p>步骤01：Redis server 启动  </p>
<p style="font-size:10px" >
 进入到实验环境中后，采用手动redis 服务启动方式 ：<p>

 <p style="font-size:10px" >&nbsp;<em> &nbsp# /usr/bin/redis-server & </em> <p>
<p style="font-size:10px" >可以看到 Redis 启动的信息， 然后回车到命令行 。 <p>
<p style="font-size:10px"> <p>
 
<p>
<p>步骤02：命令行测试Redis 服务  </p>
<p style="font-size:10px" >
在命令行输入 ： <p> 
<p style="font-size:10px"><em>&nbsp; #redis-cli      </em></p>
<p style="font-size:10px" ><p>
<p style="font-size:10px" >会显示 ： 进入Redis 命令行交互方式 。  <p>
<p style="font-size:10px" > 我们先通过命令行方式进行Redis 存取测试 ：<p>
<p style="font-size:10px" ><em>127.0.0.1:6379></em><em> set firstkey "Hello World" </em> <p>
<p style="font-size:10px" >显示 ： OK  <p>
 
<p style="font-size:10px" >这说明使用Redis 为 Firstkey 赋值成功 。<p>
<p style="font-size:10px" ><em>127.0.0.1:6379></em><em> get firstkey   </em> 
<p>
<p style="font-size:10px" >显示 ： "Hello World "  <p>
<p style="font-size:10px" >说明成功从 Rdis 获取 firstkey 的值 。   <p>
<p style="font-size:10px" >退出Redis 命令行方式：   <p>
<p style="font-size:10px" ><em>127.0.0.1:6379></em><em> exit   </em>
<p>

<p>
<p>步骤03：准备JAVA 调用Redis服务的环境  </p>
<p>
<p style="font-size:10px" >确认已经安装 JAVA 的 编译、运行环境
<p>
<p style="font-size:10px" ><em># yum list installed | grep -i java </em>
<p>
<p style="font-size:10px" >会看到JAVA 的编译、运行环境已经就绪 ：  <p>
<p style="font-size:10px" >java-1.8.0-openjdk.x86_64               1:1.8.0.302.b08-0.el7_9         @updates <p>
<p style="font-size:10px" >java-1.8.0-openjdk-devel.x86_64         1:1.8.0.302.b08-0.el7_9         @updates <p>
 <p style="font-size:10px" >转到实验目录 ：/root/java  <p>
<p style="font-size:10px" ><em>#cd /root/java  </em>  
<p style="font-size:10px" ><em>#ls -l  </em>  
<p style="font-size:10px" >可以看到 ：  <p>
<p style="font-size:10px" > RedisJava.java （这是测试Java测试Redis 服务的测试程序）  <p>
<p style="font-size:10px" > RedisStringJava.java （这是Java调用Redis 服务的测试程序）  <p>
<p style="font-size:10px" > jedis-2.9.0.jar（这是测试Java调用Redis 的接口程序， 或称驱动程序 ）  <p>
<p style="font-size:10px" >设置环境变量：  <p>
<p style="font-size:10px" ><em>#</em><em> export CLASSPATH=.:/lib/tools.jar:/lib/dt.jar:/root/java/jedis-2.9.0.jar   </em> <p>
<p style="font-size:10px" >可以通过如下命令测试验证 CLASSPATH 设置正确：  <p>
<p style="font-size:10px" ><em>#</em><em> env | grep CLASSPATH   </em> <p>
<p>
<p>

<p>步骤04：JAVA 测试 Redis服务  </p>
<p>
<p style="font-size:10px" >通过测试程序进行Redis 连接和服务状态测试<p>
<p style="font-size:10px" >测试程序编译 ：<p>
<p style="font-size:10px" ><em>#</em><em> javac RedisJava.java   </em> <p>
 <p style="font-size:10px" >没有告警、错误信息说明Java 测试程序编译成功 。 <p>
 <p style="font-size:10px" >测试程序运行 ：<p>
<p style="font-size:10px" ><em>#</em><em> java RedisJava   </em> <p>
 <p style="font-size:10px" >显示如下信息： <p>
 <p style="font-size:10px" >Succeed to connect <p>
<p style="font-size:10px" >	Redis service is active  PONG <p>


<p>步骤05：JAVA 调用 Redis服务  </p>
<p>
<p style="font-size:10px" >测试对 Redis 内容的存取：<p>
<p style="font-size:10px" >测试程序编译 ：<p>
<p style="font-size:10px" ><em>#</em><em> javac   RedisStringJava.java  </em> <p>
 <p style="font-size:10px" >没有告警、错误信息说明Java 测试程序编译成功 。 <p>
  <p style="font-size:10px" >该测试程序向Redis 写入字符串“Hello Worl”， 再读取该字符串， 验证JAVA 正常访问Redis 服务 。 <p>
 <p style="font-size:10px" >测试程序运行 ：<p>
<p style="font-size:10px" ><em>#</em><em> java RedisStringJava   </em> <p>
 <p style="font-size:10px" >显示如下信息： <p>
 <p style="font-size:10px" >Succeed to connect <p>
<p style="font-size:10px" >	The string in redis is : Hello World  <p>
<p style="font-size:10px" >这说明 JAVA 成功调用 Redis 服务。<p>
<p style="font-size:10px" >感兴趣的同学可以查看这两个测试程序的代码， 了解具体实现方式 。<p>

